function tl(p,x,y)
	if p.tx+x<1
		or p.ty+y<1
		or p.tx+x>#tiles
		or p.ty+y>#tiles[p.tx+x] then
		return 0
	elseif tiles[p.tx+x][p.ty+y]!=0 then
		if tiles[p.tx+x][p.ty+y].w!=nil then
			return tiles[p.tx+x][p.ty+y].w
		end
	else
		return 0
	end
end

