function makecloud()
	local cloud ={}
	cloud.x = rnd(400)
	cloud.y = rnd(32)-56
	cloud.vx =rnd(1)
	cloud.r =rnd(6)+4
	
	cloud.x1=cloud.x+(rnd(24)-12)
	cloud.y1=cloud.y+(rnd(8)-4)
	cloud.r1=cloud.r+(rnd(4)-2)
	cloud.x2=cloud.x+(rnd(24)-12)
	cloud.y2=cloud.y+(rnd(8)-4)
	cloud.r2=cloud.r+(rnd(4)-2)

	cloud.update = function()
		cloud.x+=cloud.vx
		cloud.x1+=cloud.vx
		cloud.x2+=cloud.vx

 		if (cloud.x>450) then
 		cloud.x -= 450
 		cloud.x1 -= 450
 		cloud.x2 -= 450
		end
	end
	
	cloud.draw=function()
		circfill(cloud.x,cloud.y,cloud.r,7)
		circfill(cloud.x1,cloud.y1,cloud.r1,7)
		circfill(cloud.x2,cloud.y2,cloud.r2,7)
	end
	
	return cloud
end

